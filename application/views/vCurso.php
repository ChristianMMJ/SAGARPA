//        }
        getSesionesActivas();

    });

    function getSesionesActivas() {
        HoldOn.open({
            theme: "sk-bounce",
            message: "CARGANDO DATOS..."
        });
        $.ajax({
            url: master_url + 'getSesionesActivasByUsuario',
            type: "POST",
            dataType: "JSON",
            data: {
                ID: idUsuario
            }
        }).done(function (data, x, jq) {
            $.each(data, function (index, item) {
                pnlNavegacionSesiones.find('#' + item.NSesion).removeClass('disabledDetalle hide');
            });
        }).fail(function (x, y, z) {
            console.log(x, y, z);
        }).always(function () {
            HoldOn.close();
        });
    }
</script>
